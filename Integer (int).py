# Integer (int)
age = 23
year = 2026

# Float
price = 99.99
tax_rate = 0.18

# String
name = "Madhu"
course = "Python Programming"

# Boolean
is_student = True
has_passport = False

# Using them together
print("Name:", name)
print("Course:", course)
print("Age:", age)
print("Year:", year)
print("Price:", price)
print("Tax Rate:", tax_rate)
print("Is Student:", is_student)
print("Has Passport:", has_passport)

# Calculations with int and float
total_price = price + (price * tax_rate)
print("Total Price with Tax:", total_price)

# String concatenation
full_info = name + " is learning " + course
print(full_info)

# Boolean logic
eligible_to_vote = age >= 18 and has_passport
print("Eligible to vote:", eligible_to_vote)
