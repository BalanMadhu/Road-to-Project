# String examples
first_name = "Madhu"
last_name = "Balan"
course = "Python Programming"

# Concatenation
full_name = first_name + " " + last_name
print("Full Name:", full_name)

# String repetition
echo = "Hi! " * 3
print("Echo:", echo)

# String methods
print("Uppercase:", course.upper())
print("Lowercase:", course.lower())
print("Title Case:", course.title())
print("Length of course string:", len(course))

# Slicing
print("First 6 letters of course:", course[:6])
print("Last 5 letters of course:", course[-2:])
