# Float (decimal number) example
price = 99.96     # assigning a float value
tax_rate = 0.18     # 18% tax as a float

# Performing arithmetic with floats
total_price = price + (price * tax_rate)
discounted_price = price - 10.5
division_result = price / 3

print("Original Price:", price)
print("Tax Rate:", tax_rate)
print("Total Price with Tax:", total_price)
print("Discounted Price:", discounted_price)
print("Division Result:", division_result)
